// Imprimir cada elemento do array em linha
const array = ['Ana Joana', 'Alberto', 'Joaquim', 'Rods', 'Nat', 'Sincero', 'Renan'];
//Sem spread operator
console.log(array[0], array[1], array[2], array[3], array[4], array[5], array[6]);
// Ana Joana Alberto Joaquim Rods Nat Sincero Renan

//Com spread operator
console.log(...array);
//Ana Joana Alberto Joaquim Rods Nat Sincero Renan
